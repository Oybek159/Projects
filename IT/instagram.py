import turtle as t
import turtle
window = t.getscreen()
t.goto(-100,-100)
t.pensize(20)
t.bgcolor("black")
t.color('white')
t.fillcolor('#ff0087')
t.begin_fill()
t.forward(300)
t.circle(50,90)
t.forward(300)
t.circle(50,90)
t.forward(300)
t.circle(50,90)
t.forward(300)
t.circle(50,90)
t.end_fill()
t.up()
t.goto(50,0)
t.down()
t.fillcolor('#ff0087')
t.begin_fill()
t.circle(100)
t.end_fill()
t.up()
t.goto(170,230)
t.down()
t.dot(50,"white")

window.mainloop()
